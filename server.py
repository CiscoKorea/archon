#===============================================================================
# Import Platform Base
#===============================================================================
import re
import os
import django
import archon.settings

os.environ.setdefault("DJANGO_SETTINGS_MODULE", "archon.settings")
django.setup()

#===============================================================================
# Init Modules & Managers
#===============================================================================
import archon.urls
import archon.wsgi

#===============================================================================
# Inject Static File Resolver
#===============================================================================
from django.conf.urls import url
from django.views.static import serve
archon.urls.urlpatterns = [ url(r'^%s(?P<path>.*)$' % re.escape(archon.settings.STATIC_URL.lstrip('/')), serve, kwargs={'document_root' : archon.settings.STATIC_DIR}) ] + archon.urls.urlpatterns

#===============================================================================
# Run Gevent WSGI Server
#===============================================================================
from gevent.pywsgi import WSGIServer
WSGIServer(('0.0.0.0', 80), archon.wsgi.application, environ=os.environ).serve_forever()
